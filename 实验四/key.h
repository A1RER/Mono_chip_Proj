/*
 * key.h  --  独立按键扫描 (KEY1=P3^2/板上K3, KEY2=P3^3/板上K4, 均低有效)
 *
 * 交互逻辑:
 *   K3 短按 -> 页面循环切换:
 *              页1 时间+温度 -> 页2 上下限显示 -> 页3 设置上限
 *              -> 页4 设置下限 -> 页5 采集状态+上传频率 -> 页1 ...
 *   K4 短按 -> 在"页3 设置上限"时, 上限 +0.5C
 *              在"页4 设置下限"时, 下限 +0.5C
 *              其他页面: 不处理
 *   K4 长按 -> 在"页3 设置上限"时, 上限 -0.5C  (按住 >=800ms 触发, 每 200ms 重复)
 *              在"页4 设置下限"时, 下限 -0.5C
 *              其他页面: 不处理
 *
 * 步长固定 0.5C (即 temp_x10 增减 5).
 * 调用方式: 主循环每个 50ms 节拍调用一次 Key_Scan().
 *
 * 说明: 板上 K1/K2 接到 P3^0(RXD)/P3^1(TXD), 串口启用后被占用, 不可用作
 * 按键. 老师建议的 3 键方案中 K3 减号功能, 这里改为 K4 长按实现.
 */

